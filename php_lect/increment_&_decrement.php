<?php

    $num = 10;

    echo "example of post increment " , $num++ , "<br>"; //post-increment
    echo "example of pre increment " , ++$num , "<br>"; //pre-increment
    echo "example of post decrement " , $num-- , "<br>"; //post-decrement
    echo "example of pre decrement " , --$num , "<br>"; //pre-decrement
    
?>